#ESTRUTURA DE REPETIÇÃO

=begin
While - enquanto a condição for verdadeira vai seguir com o bloco de repetição
Until - enquanto for falso vai seguir com o bloco de repetição
=end

i = 1
while i <= 10
    puts "incrementando #{i}"
    i += 1
end


j = 10
until j <= 1
    puts "decrementando #{j}"
    j -= 1
end
     

